"""GIR → Docusaurus MDX API-reference generator.

Consumes the same overlay-applied :class:`~ginext_stubgen.Namespace` model the
``.pyi`` emitter uses (via :func:`ginext_stubgen.build_namespace` in ``raw``
doc mode), so the Python signatures, docs, and overlays on an API page can never
drift from the stubs.

Output layout (one directory per namespace, one page per class), consumed by the
Docusaurus ``api`` docs plugin whose sidebar is autogenerated from the tree::

    content/api/
      index.mdx                 # namespace roster
      Gtk/
        _category_.json
        index.mdx               # functions, enums, constants, callbacks
        Window.mdx              # one page per class/interface/record/union
        ...

Cross-references (gtk-doc ``[class@Gtk.Window]`` link syntax and legacy C
identifiers such as ``gtk_window_new()`` / ``#GtkWidget``) resolve to real,
file-relative Markdown links **only when the target page was generated** in this
run; otherwise they fall back to inline code, so the Docusaurus build never sees
a broken page link.
"""

from __future__ import annotations

import json
import os
import re
from collections import defaultdict
from typing import TYPE_CHECKING

from . import Callable, DocRef, Klass, Namespace, Parser, Property, Signal
from . import render_signature

if TYPE_CHECKING:
    from pathlib import Path

# Kinds that get their own page; everything else lives on the namespace index.
_CLASS_KINDS = {"class", "interface", "record", "union"}

# gtk-doc modern link syntax: [kind@Target], optionally fenced in backticks and
# with a trailing () for callables.
_LINK_AT_RE = re.compile(
    r"\[`?(?P<kind>func|method|ctor|const|type|class|iface|struct|enum|flags"
    r"|callback|error|property|signal|vfunc|alias)@(?P<target>[A-Za-z0-9_.:%-]+)"
    r"`?\]"
)
# Legacy sigils.
_TYPE_SIGIL_RE = re.compile(r"#([A-Z][A-Za-z0-9_]+)\b")
_CONST_SIGIL_RE = re.compile(r"%([A-Za-z_][A-Za-z0-9_]*)\b")
_ARG_SIGIL_RE = re.compile(r"@([A-Za-z_][A-Za-z0-9_]*)\b")
# Old pango/C inline code blocks: |[ <!-- language="C" --> ... ]|
_PANGO_FENCE_RE = re.compile(r"\|\[(?:\s*<!--.*?-->)?(.*?)\]\|", re.S)
_FENCE_RE = re.compile(r"```.*?```", re.S)
_INLINE_CODE_RE = re.compile(r"`[^`\n]+`")
_IMAGE_RE = re.compile(r"!\[([^\]]*)\]\([^)]*\)")
# A Markdown link whose target is a legacy gtk-doc HTML page (the doc text
# carries these verbatim, e.g. ``[I/O priority](iface.AsyncResult.html#frag)``).
# Link text may wrap across lines in the source, so newlines are allowed in it.
_MD_HTML_LINK_RE = re.compile(r"\[([^\]]+)\]\(([^)\s]*\.html[^)\s]*)\)")
# gi-docgen HTML basenames: ``<kind>.<Rest>.html``.
_GTKDOC_TYPE_KINDS = {
    "class",
    "iface",
    "interface",
    "struct",
    "union",
    "enum",
    "flags",
    "error",
    "alias",
}
_GTKDOC_MEMBER_KINDS = {
    "method": "method",
    "ctor": "ctor",
    "vfunc": "vfunc",
    "property": "property",
    "signal": "signal",
    "type_func": None,  # static function — the anchor picker finds ``static-``
}


def _slug(name: str) -> str:
    return name.replace(":", "-")


class DocEmitter:
    """Render a set of parsed namespaces into a Docusaurus ``content/api`` tree."""

    def __init__(self, entries: list[tuple[Namespace, Parser]], *, ext: str = "mdx"):
        self.entries = entries
        # Page file extension: "mdx" for Docusaurus, "md" for mkdocs. Drives both
        # the written filenames and the relative cross-reference link targets.
        self.ext = ext.lstrip(".")
        self.namespaces: set[str] = {ns.name for ns, _ in entries}

        # Merged C-name → Python-target maps, used to resolve legacy doc refs.
        self.c_identifiers: dict[str, DocRef] = {}
        self.c_types: dict[str, str] = {}
        self.c_constants: dict[str, str] = {}
        # (namespace, classname) pairs that have their own page.
        self.class_pages: set[tuple[str, str]] = set()
        # namespace → {top-level name: kind}
        self.top_kind: dict[str, dict[str, str]] = defaultdict(dict)
        # page key (namespace, classname|None) → set of anchor ids the page
        # actually renders. Cross-references resolve only to anchors in this
        # registry, so a link never points at a member that the target page
        # doesn't define (e.g. a constructor rendered as ``ctor-`` not
        # ``method-``, or an inherited member that lives on a parent's page).
        self.anchors: dict[tuple[str, str | None], set[str]] = {}
        # Full Python path of a moved-to function → the class member it duplicates
        # (e.g. "Gio.app_info_get_all" → "Gio.AppInfo.get_all"). Such functions are
        # hidden from the namespace index and their refs redirect to the class.
        self.moved_to: dict[str, str] = {}

        for ns, parser in entries:
            identifiers, types, constants = parser.doc_ref_maps()
            self.c_identifiers.update(identifiers)
            self.c_types.update(types)
            self.c_constants.update(constants)
            index_anchors: set[str] = set()
            for enum in ns.enums:
                self.top_kind[ns.name][enum.name] = "enum"
                index_anchors.add(f"enum-{enum.name}")
            for fn in ns.functions:
                if fn.moved_to:
                    self.moved_to[f"{ns.name}.{fn.name}"] = f"{ns.name}.{fn.moved_to}"
                    continue
                self.top_kind[ns.name][fn.name] = "function"
                index_anchors.add(f"function-{fn.name}")
            for const in ns.constants:
                self.top_kind[ns.name][const.name] = "constant"
                index_anchors.add(f"constant-{const.name}")
            for cb in ns.callbacks:
                self.top_kind[ns.name][cb.name] = "callback"
                index_anchors.add(f"callback-{cb.name}")
            self.anchors[(ns.name, None)] = index_anchors
            for klass in ns.classes:
                self.top_kind[ns.name][klass.name] = "class"
                self.class_pages.add((ns.name, klass.name))
                self.anchors[(ns.name, klass.name)] = self._class_anchors(klass)

    @staticmethod
    def _class_anchors(klass: Klass) -> set[str]:
        anchors: set[str] = set()
        for fn in klass.constructors:
            anchors.add(f"ctor-{fn.name}")
        for fn in klass.methods:
            anchors.add(f"method-{fn.name}")
        for fn in klass.static_functions:
            anchors.add(f"static-{fn.name}")
        for fn in klass.virtual_methods:
            anchors.add(f"vfunc-{fn.name}")
        for prop in klass.properties:
            anchors.add(f"property-{prop.py_name}")
        for sig in klass.signals:
            anchors.add(f"signal-{_slug(sig.name)}")
        return anchors

    # -- public API ---------------------------------------------------------

    def write(self, out_dir: Path) -> list[Path]:
        """Write the full tree under ``out_dir`` and return the files written."""
        out_dir.mkdir(parents=True, exist_ok=True)
        written: list[Path] = []
        for ns, _parser in self.entries:
            written += self._write_namespace(ns, out_dir)
        written.append(self._write_root_index(out_dir))
        return written

    # -- namespace / class pages -------------------------------------------

    def _write_namespace(self, ns: Namespace, out_dir: Path) -> list[Path]:
        ns_dir = out_dir / ns.name
        ns_dir.mkdir(parents=True, exist_ok=True)
        written: list[Path] = []

        # Docusaurus sidebar metadata. mkdocs derives the section nav from the
        # directory tree (mkdocs-literate-nav), so it needs no sidecar.
        if self.ext != "md":
            category = {
                "label": ns.name,
                "link": {"type": "doc", "id": f"{ns.name}/index"},
            }
            cat_path = ns_dir / "_category_.json"
            cat_path.write_text(
                json.dumps(category, indent=2) + "\n", encoding="utf-8"
            )
            written.append(cat_path)

        written.append(self._write_namespace_index(ns, ns_dir))
        for klass in sorted(ns.classes, key=lambda k: k.name):
            written.append(self._write_class(ns, klass, ns_dir))
        return written

    def _write_namespace_index(self, ns: Namespace, ns_dir: Path) -> Path:
        out: list[str] = [
            "---",
            f"title: {ns.name}",
            "---",
            "",
            f"# {ns.name}",
            "",
            f"API reference for the `{ns.name}` namespace "
            f"(version {ns.version}), generated from GObject-Introspection.",
            "",
        ]
        cur = (ns.name, None)

        if ns.classes:
            out += ["## Classes", ""]
            for klass in sorted(ns.classes, key=lambda k: k.name):
                link = self._rel(cur, (ns.name, klass.name))
                summary = self._summary(klass.doc, cur)
                out.append(
                    f"- [{klass.name}]({link})" + (f" — {summary}" if summary else "")
                )
            out.append("")

        # Skip moved-to functions: they are convenience duplicates of a type's
        # static method and are shown on that class's page instead.
        functions = [fn for fn in ns.functions if not fn.moved_to]
        if functions:
            out += ["## Functions", ""]
            for fn in sorted(functions, key=lambda f: f.name):
                out += self._callable_block(
                    fn, instance=None, kind="function", current=cur
                )
            out.append("")

        if ns.enums:
            out += ["## Enumerations", ""]
            for enum in sorted(ns.enums, key=lambda e: e.name):
                kind = "flags" if enum.is_flags else "enum"
                out.append(f"### {enum.name} {{#enum-{enum.name}}}")
                out.append("")
                out.append(f"_{('Bit flags' if enum.is_flags else 'Enumeration')}._")
                out.append("")
                for member in enum.values:
                    line = f"- `{member.name}` = `{member.value}`"
                    mdoc = self._to_markdown(member.doc, cur, inline=True)
                    if mdoc:
                        line += f" — {mdoc}"
                    out.append(line)
                out.append("")
                _ = kind

        if ns.constants:
            out += ["## Constants", ""]
            for const in sorted(ns.constants, key=lambda c: c.name):
                out.append(f"### {const.name} {{#constant-{const.name}}}")
                out.append("")
                out.append(f"```python\n{const.name}: {const.type_expr}\n```")
                out.append("")
                cdoc = self._to_markdown(const.doc, cur)
                if cdoc:
                    out += [cdoc, ""]

        if ns.callbacks:
            out += ["## Callbacks", ""]
            for cb in sorted(ns.callbacks, key=lambda c: c.name):
                params = ", ".join(f"{p.name}: {p.type_expr}" for p in cb.params)
                out.append(f"### {cb.name} {{#callback-{cb.name}}}")
                out.append("")
                out.append(
                    f"```python\n{cb.name} = Callable[[{params}], {cb.return_expr}]\n```"
                )
                out.append("")

        path = ns_dir / f"index.{self.ext}"
        path.write_text("\n".join(out).rstrip() + "\n", encoding="utf-8")
        return path

    def _write_class(self, ns: Namespace, klass: Klass, ns_dir: Path) -> Path:
        cur = (ns.name, klass.name)
        kind_label = {
            "class": "class",
            "interface": "interface",
            "record": "record (struct)",
            "union": "union",
        }.get(klass.kind, klass.kind)

        out: list[str] = [
            "---",
            f"title: {ns.name}.{klass.name}",
            "---",
            "",
            f"# {ns.name}.{klass.name}",
            "",
            f"_{kind_label}_"
            + (
                " — extends "
                + ", ".join(self._type_link(p, cur) for p in klass.parents)
                if klass.parents
                else ""
            ),
            "",
        ]
        if klass.deprecated:
            out += [self._deprecation_note(klass.deprecated_version), ""]
        cdoc = self._to_markdown(klass.doc, cur)
        if cdoc:
            out += [cdoc, ""]

        out += self._members_section(
            "Constructors", klass.constructors, instance="cls", kind="ctor", current=cur
        )
        out += self._members_section(
            "Methods", klass.methods, instance="self", kind="method", current=cur
        )
        out += self._members_section(
            "Static functions",
            klass.static_functions,
            instance=None,
            kind="static",
            current=cur,
        )
        out += self._members_section(
            "Virtual methods",
            klass.virtual_methods,
            instance="self",
            kind="vfunc",
            current=cur,
        )
        out += self._properties_section(klass.properties, cur)
        out += self._signals_section(klass.signals, cur)

        path = ns_dir / f"{klass.name}.{self.ext}"
        path.write_text("\n".join(out).rstrip() + "\n", encoding="utf-8")
        return path

    def _write_root_index(self, out_dir: Path) -> Path:
        out = [
            "---",
            "title: API reference",
            "---",
            "",
            "# API reference",
            "",
            "Python API for the ginext namespaces, generated from "
            "GObject-Introspection and the ginext overlays.",
            "",
            "## Namespaces",
            "",
        ]
        for ns, _ in sorted(self.entries, key=lambda e: e[0].name):
            out.append(
                f"- [{ns.name}](./{ns.name}/index.{self.ext}) — version {ns.version}"
            )
        out.append("")
        path = out_dir / f"index.{self.ext}"
        path.write_text("\n".join(out) + "\n", encoding="utf-8")
        return path

    # -- member rendering ---------------------------------------------------

    def _members_section(
        self,
        title: str,
        items: list[Callable],
        *,
        instance: str | None,
        kind: str,
        current: tuple[str, str | None],
    ) -> list[str]:
        if not items:
            return []
        out = [f"## {title}", ""]
        for fn in items:
            out += self._callable_block(
                fn, instance=instance, kind=kind, current=current
            )
        return out

    def _callable_block(
        self,
        fn: Callable,
        *,
        instance: str | None,
        kind: str,
        current: tuple[str, str | None],
    ) -> list[str]:
        sig = render_signature(fn, instance=instance)
        decorator = ""
        if kind == "static":
            decorator = "@staticmethod\n"
        elif kind == "ctor":
            decorator = "@classmethod\n"
        out = [
            f"### {fn.name} {{#{kind}-{fn.name}}}",
            "",
            f"```python\n{decorator}def {fn.name}{sig}\n```",
            "",
        ]
        if fn.deprecated:
            out += [self._deprecation_note(fn.deprecated_version), ""]
        doc = self._to_markdown(fn.doc, current)
        if doc:
            out += [doc, ""]
        param_docs = [
            (p.name, self._to_markdown(p.doc, current, inline=True)) for p in fn.params
        ]
        param_docs = [(n, d) for n, d in param_docs if d]
        if param_docs:
            out.append("**Parameters:**")
            out.append("")
            for pname, pdoc in param_docs:
                out.append(f"- `{pname}` — {pdoc}")
            out.append("")
        return out

    def _properties_section(
        self, props: list[Property], current: tuple[str, str | None]
    ) -> list[str]:
        if not props:
            return []
        out = ["## Properties", ""]
        for prop in props:
            access = "read/write" if prop.writable else "read-only"
            out.append(f"### {prop.py_name} {{#property-{prop.py_name}}}")
            out.append("")
            out.append(f"```python\n{prop.py_name}: {prop.type_expr}  # {access}\n```")
            out.append("")
            if prop.deprecated:
                out += [self._deprecation_note(prop.deprecated_version), ""]
            doc = self._to_markdown(prop.doc, current)
            if doc:
                out += [doc, ""]
        return out

    def _signals_section(
        self, signals: list[Signal], current: tuple[str, str | None]
    ) -> list[str]:
        if not signals:
            return []
        out = ["## Signals", ""]
        for sig in signals:
            handler = ", ".join(
                ["self"] + [f"{p.name}: {p.type_expr}" for p in sig.params]
            )
            out.append(f"### {sig.name} {{#signal-{_slug(sig.name)}}}")
            out.append("")
            out.append(
                f"```python\ndef on_{_slug(sig.name).replace('-', '_')}"
                f"({handler}) -> {sig.return_expr}: ...\n```"
            )
            out.append("")
            if sig.deprecated:
                out += [self._deprecation_note(sig.deprecated_version), ""]
            doc = self._to_markdown(sig.doc, current)
            if doc:
                out += [doc, ""]
        return out

    def _deprecation_note(self, version: str | None) -> str:
        suffix = f" since {version}" if version else ""
        return f":::warning Deprecated{suffix}\nThis API is deprecated.\n:::"

    def _summary(self, doc: str | None, current: tuple[str, str | None]) -> str:
        md = self._to_markdown(doc, current, inline=True)
        if not md:
            return ""
        first = md.split("\n\n", 1)[0].replace("\n", " ").strip()
        # Reduce to plain text: drop link/image syntax and bare URLs so a
        # truncated summary can't emit a malformed GFM autolink (which breaks
        # the MDX build), and so the list item isn't a link-inside-a-link.
        first = re.sub(r"\[`?([^\]`]*)`?\]\([^)]*\)", r"\1", first)
        first = re.sub(r"https?://\S+", "", first)
        first = re.sub(r"\s{2,}", " ", first).strip()
        if len(first) > 140:
            first = first[:140].rsplit(" ", 1)[0] + "…"
        return first

    # -- cross-reference resolution ----------------------------------------

    def _rel(
        self,
        current: tuple[str, str | None],
        target_page: tuple[str, str | None],
        anchor: str | None = None,
    ) -> str:
        """File-relative Docusaurus link from the current page to a target page."""
        cur_dir = (current[0],)  # both class pages and the index live in <NS>/
        tns, tcls = target_page
        target = (tns, tcls if tcls is not None else "index")
        rel = os.path.relpath("/".join(target), "/".join(cur_dir))
        if not rel.startswith("."):
            rel = "./" + rel
        rel += f".{self.ext}"
        return rel + (f"#{anchor}" if anchor else "")

    def _resolve(
        self,
        target_path: str,
        current: tuple[str, str | None],
        member_kind: str | None = None,
    ) -> tuple[str, str] | None:
        """Resolve a ``NS.Name[.member]`` path to (url, label), or None."""
        # A ref to a moved-to function points at the class member it duplicates.
        target_path = self.moved_to.get(target_path, target_path)
        parts = target_path.split(".")
        ns = parts[0]
        if ns not in self.namespaces:
            return None
        label = (
            ".".join(parts[1:]) if ns == current[0] and len(parts) > 1 else target_path
        )

        if len(parts) >= 2 and (ns, parts[1]) in self.class_pages:
            page: tuple[str, str | None] = (ns, parts[1])
            anchor: str | None = None
            if len(parts) >= 3:
                # None when the member isn't on the page (inherited, filtered):
                # _rel then emits a page-level link, never a broken anchor.
                anchor = self._pick_member_anchor(page, parts[2], member_kind)
            return self._rel(current, page, anchor), label

        index = (ns, None)
        index_anchors = self.anchors.get(index, set())
        kind = self.top_kind.get(ns, {}).get(parts[1]) if len(parts) >= 2 else None
        if kind in ("enum", "function", "constant", "callback"):
            candidate = f"{kind}-{parts[1]}"
            anchor = candidate if candidate in index_anchors else None
            return self._rel(current, index, anchor), label
        if len(parts) == 1:
            return self._rel(current, index), label
        return None

    def _pick_member_anchor(
        self, page: tuple[str, str | None], member: str, member_kind: str | None
    ) -> str | None:
        """Pick the anchor the target page actually renders for ``member``.

        The cross-reference's kind hint is unreliable — a legacy C identifier
        carries role ``meth`` even for constructors and static functions — so we
        probe every section's prefix and let the page's anchor registry decide.
        Returns None when no matching anchor exists on the page.
        """
        existing = self.anchors.get(page, set())
        und = member.replace("-", "_")
        ordered: list[str] = []
        if member_kind == "property":
            ordered.append(f"property-{und}")
        elif member_kind == "signal":
            ordered += [f"signal-{member}", f"signal-{und}"]
        elif member_kind == "vfunc":
            ordered.append(f"vfunc-{member}")
        elif member_kind == "ctor":
            ordered.append(f"ctor-{member}")
        ordered += [
            f"method-{member}",
            f"ctor-{member}",
            f"static-{member}",
            f"vfunc-{member}",
            f"property-{und}",
            f"signal-{member}",
            f"signal-{und}",
        ]
        for candidate in ordered:
            if candidate in existing:
                return candidate
        return None

    def _type_link(self, py_type: str, current: tuple[str, str | None]) -> str:
        resolved = self._resolve(py_type, current)
        if resolved is None:
            return f"`{py_type}`"
        url, label = resolved
        return f"[`{label}`]({url})"

    # -- gtk-doc → Markdown -------------------------------------------------

    def _to_markdown(
        self, text: str | None, current: tuple[str, str | None], *, inline: bool = False
    ) -> str:
        if not text:
            return ""
        text = _PANGO_FENCE_RE.sub(
            lambda m: f"\n```\n{m.group(1).strip()}\n```\n", text
        )
        # Drop gtk-doc image references — the bitmaps aren't shipped with the API
        # pages, and Docusaurus errors on unresolved local images. Keep the alt
        # text as an italic caption when present.
        text = _IMAGE_RE.sub(
            lambda m: f"*{m.group(1)}*" if m.group(1).strip() else "", text
        )

        store: dict[str, str] = {}

        def _protect(regex: re.Pattern[str], tag: str, src: str) -> str:
            def repl(m: re.Match[str]) -> str:
                key = f"\x00{tag}{len(store)}\x00"
                store[key] = m.group(0)
                return key

            return regex.sub(repl, src)

        text = _protect(_FENCE_RE, "F", text)
        text = _protect(_INLINE_CODE_RE, "C", text)

        # MDX treats <, {, } as JSX. Escape them in flowed prose (code spans and
        # fences are already protected above and render literally).
        text = text.replace("<", "&lt;").replace("{", "&#123;").replace("}", "&#125;")

        # Demote in-body gtk-doc headings by two levels so they nest under the
        # page H1 and section H2s instead of colliding with them.
        text = re.sub(
            r"(?m)^(#{1,6})(\s)",
            lambda m: "#" * min(len(m.group(1)) + 2, 6) + m.group(2),
            text,
        )

        text = self._render_refs(text, current)

        for key, original in store.items():
            text = text.replace(key, original)

        text = text.strip()
        if inline:
            text = re.sub(r"\s*\n\s*", " ", text)
        return text

    def _resolve_gtkdoc_html(
        self, url: str, current: tuple[str, str | None]
    ) -> str | None:
        """Map a gi-docgen HTML link to our page, or None if it can't resolve.

        Handles same-namespace ``<kind>.<Rest>.html`` basenames (the common
        case in GIR doc text). Cross-namespace links (``../glib/…``) and
        standalone gtk-doc pages (``overview.html``) return None so the caller
        drops them to plain text rather than emitting a broken link.
        """
        base = url.split("#", 1)[0]
        if "/" in base or not base.endswith(".html"):
            return None
        parts = base[: -len(".html")].split(".")
        kind, rest = parts[0], parts[1:]
        ns = current[0]
        if kind in _GTKDOC_TYPE_KINDS and len(rest) == 1:
            target, member_kind = f"{ns}.{rest[0]}", None
        elif kind in _GTKDOC_MEMBER_KINDS and len(rest) == 2:
            target, member_kind = (
                f"{ns}.{rest[0]}.{rest[1]}",
                _GTKDOC_MEMBER_KINDS[kind],
            )
        elif kind in ("func", "const") and len(rest) == 1:
            target, member_kind = f"{ns}.{rest[0]}", None
        else:
            return None
        resolved = self._resolve(target, current, member_kind)
        return resolved[0] if resolved else None

    def _render_refs(self, text: str, current: tuple[str, str | None]) -> str:
        def html_link(m: re.Match[str]) -> str:
            label, url = m.group(1), m.group(2)
            resolved = self._resolve_gtkdoc_html(url, current)
            return f"[{label}]({resolved})" if resolved else label

        # Rewrite legacy gtk-doc HTML links before anything else: resolve the
        # same-namespace ones to our pages, drop the rest to their link text.
        text = _MD_HTML_LINK_RE.sub(html_link, text)

        def render_at(m: re.Match[str]) -> str:
            kind = m.group("kind")
            target = m.group("target")
            member_kind = None
            if kind in {"method", "vfunc", "ctor"}:
                member_kind = "method" if kind == "method" else kind
            if kind == "property":
                target = target.replace(":", ".")
                member_kind = "property"
            elif kind == "signal":
                target = target.replace("::", ".")
                member_kind = "signal"
            resolved = self._resolve(target, current, member_kind)
            if resolved is None:
                return f"`{target}`"
            url, label = resolved
            return f"[`{label}`]({url})"

        text = _LINK_AT_RE.sub(render_at, text)

        # Legacy C identifiers (empty-call form first, then bare).
        if self.c_identifiers:
            names = sorted(map(re.escape, self.c_identifiers), key=len, reverse=True)
            pattern = "|".join(names)

            def ident(m: re.Match[str]) -> str:
                ref = self.c_identifiers[m.group("name")]
                member_kind = "method" if ref.role == "meth" else None
                resolved = self._resolve(ref.target, current, member_kind)
                if resolved is None:
                    return f"`{ref.target}`"
                url, label = resolved
                return f"[`{label}`]({url})"

            text = re.sub(rf"\b(?P<name>{pattern})\s*\(\s*\)", ident, text)
            text = re.sub(rf"\b(?P<name>{pattern})\b", ident, text)

        text = _CONST_SIGIL_RE.sub(lambda m: self._const(m.group(1), current), text)
        text = _TYPE_SIGIL_RE.sub(lambda m: self._ctype(m.group(1), current), text)
        text = _ARG_SIGIL_RE.sub(r"`\1`", text)
        return text

    def _const(self, name: str, current: tuple[str, str | None]) -> str:
        if name == "TRUE":
            return "`True`"
        if name == "FALSE":
            return "`False`"
        if name in {"NULL", "nullptr"}:
            return "`None`"
        py = self.c_constants.get(name)
        if py:
            resolved = self._resolve(py, current)
            if resolved:
                url, label = resolved
                return f"[`{label}`]({url})"
        return f"`{name}`"

    def _ctype(self, name: str, current: tuple[str, str | None]) -> str:
        if name in {"NULL", "nullptr"}:
            return "`None`"
        if name in {"TRUE", "FALSE"}:
            return f"`{name.capitalize()}`"
        py = self.c_types.get(name)
        if py:
            resolved = self._resolve(py, current)
            if resolved:
                url, label = resolved
                return f"[`{label}`]({url})"
        return f"`{name}`"
